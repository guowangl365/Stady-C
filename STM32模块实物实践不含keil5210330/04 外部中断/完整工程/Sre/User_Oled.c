//-------------------------------------------------------------------------------------------------------------------
//  @brief      汉字显示16x16
//  @param      x       横坐标 0-127
//  @param      y       纵坐标 0-7
//  @param      ch      汉字
//  @since      v1.0
//  Sample usage:		
//  @Note       	    
//-------------------------------------------------------------------------------------------------------------------
void OLED_P16x16Str(uint8 x,uint8 y,uint8 ch[])
{
	uint8  wz=0,i = 0;
	uint16 adder=1;
        
	while(ch[i] != '\0')
	{
  	wz = 0;
  	adder = 1;
  	while(F16x16_Idx[wz] > 127)//一个汉字占两个字节
  	{
  		if(F16x16_Idx[wz] == ch[i])
  		{
  			if(F16x16_Idx[wz + 1] == ch[i + 1])
  			{
  				adder = wz * 16;
  				break;
  			}
  		}
  		wz += 2;
  	}
  	
        if(x>118){x=0;y++;}
  	OLED_Set_Pos(x , y);
  	
    if(adder != 1)  {        // 显示汉字
  		  OLED_Set_Pos(x , y);
  		  for(wz = 0;wz < 16;wz++)  {
  			     OLED_WrDat(F16x16[adder]);
  			     adder += 1;}
  		
			  OLED_Set_Pos(x , y + 1);
  		  for(wz = 0;wz < 16;wz++)  {
  			     OLED_WrDat(F16x16[adder]);
  			     adder += 1;}
  	}
  	
		else  {			  //显示空白字符
  		  i += 1;
        OLED_Set_Pos(x , y);
  		  for(wz = 0;wz < 16;wz++)  {OLED_WrDat(0);}
  		
			  OLED_Set_Pos(x , y + 1);
  		  for(wz = 0;wz < 16;wz++)  {OLED_WrDat(0);}
  	}
  	x += 16;
  	i += 2;
	}
}


