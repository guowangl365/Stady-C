//内部使用用户无需调用
void OLED_WrCmd(uint8 cmd)
{
	uint8 i=8;

	OLED_DC_Clr();
	OLED_SCL_Clr();
	
	while(i--) {
		if(cmd&0x80) {OLED_SDA_Set();}
		else {OLED_SDA_Clr();}
		OLED_SCL_Set();
		OLED_SCL_Clr();
		cmd<<=1;
	}
}

//内部使用用户无需调用
void OLED_WrDat(uint8 data)
{
	uint8 i=8;

	OLED_DC_Set();
	OLED_SCL_Clr();
	
	while(i--) {
		  if(data&0x80) {OLED_SDA_Set();}
		  else {OLED_SDA_Clr();}
		  OLED_SCL_Set();
		  OLED_SCL_Clr();
	  	data<<=1;
	}
}

//内部使用用户无需调用
void OLED_HEXACSII(uint16 hex,uint8* Print)
{
	uint8 hexcheck ;
	uint8 TEMP ;
	TEMP = 6 ;
	Print[TEMP ]='\0';
	while(TEMP) {
	  	TEMP -- ;
	  	hexcheck  =  hex%10 ;
	  	hex   /=10 ;
	  	Print[TEMP ]  = hexcheck + 0x30 ;
	}
  
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      OLED清屏函数
//  @param      bmp_data	填充颜色选着(0x00 or 0xff)
//  @return     void
//  @since      v1.0
//  Sample usage:			
//-------------------------------------------------------------------------------------------------------------------
void OLED_fill(uint8 bmp_data)
{
	uint8 y,x;
	
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<128;x++)	OLED_WrDat(bmp_data); 
	}
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      OLED显示坐标设置
//  @param      x			x轴坐标设置
//  @param      y			y轴坐标设置
//  @return     void
//  @since      v1.0
//  Sample usage:			
//-------------------------------------------------------------------------------------------------------------------
void OLED_Set_Pos(uint8 x, uint8 y)
{
  OLED_WrCmd(0xb0+y);
  OLED_WrCmd(((x&0xf0)>>4)|0x10);
  OLED_WrCmd((x&0x0f)|0x01);
}

